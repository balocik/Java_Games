    //method to display whether user entered correct or incorrect answer
    private void guessEntere(){
        if(correct.equals(txtGuessWord.getText()) && (counter > 1)){
            lblGoodBadGuess.setText("You WON!!! \"" + txtGuessWord.getText() + "\" is a correct answer");
            counter--;
            lblCounter.setText("" + (counter));
            btnGuess.setEnabled(false);
            btnGiveUp.setEnabled(false);
        }else if(counter == 1){
            lblGoodBadGuess.setText("You LOST!!! \"" + correct + "\" is a correct answer");
            lblCounter.setText("" + (--counter));
            btnGuess.setEnabled(false);
            btnGiveUp.setEnabled(false);
        }else{
            lblGoodBadGuess.setText("Try again \"" + txtGuessWord.getText() + "\" is a wrong answer");
            counter--;
            lblCounter.setText("" + (counter));
        }
    }
    
    
