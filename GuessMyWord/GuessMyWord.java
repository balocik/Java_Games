    //program takes original word from previous JFrame and sets jumble word
    public void setMisteryWord(String text){
        this.misteryWord = text;
        correct = misteryWord;
        lblMisteryWord.setText(jumbleString());
    }
    
    //method to mix letters in original word
    private String jumbleString(){
        String jumble = "";
        while(misteryWord.length() !=0){
            int position = (int)(Math.random() * misteryWord.length());
            jumble += misteryWord.charAt(position);
            misteryWord = misteryWord.substring(0,position) + misteryWord.substring(position + 1);
        }
        return jumble;
    }
    
    
